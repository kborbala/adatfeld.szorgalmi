# Három szám definiálása
szam1 <- 20
szam2 <- 15
szam3 <- 22
szam4 <- 11

# Átlag számítása
atlag <- (szam1 + szam2 + szam3 + szam4) / 4 #a három szám kiegészítése egy negyedik számmal, és a képlet javítása

# Eredmény kiíratása
print(paste("A négy szám átlaga:", atlag)) #móddosítás háromról négyre
